// Math operations

console.log(10 + 4);
console.log(10 - 4);
console.log(10 * 4);
console.log(10 / 4);
console.log(10 % 4); // 10 / 4 => 2 * 4 => Remainder: 2
console.log(10 % 3); // Expected Remainder: 1

console.log((10 + 3 - 5) * 10); // Les règles de mathématiques classiques s'appliquent.

let result = (10 + 3 - 5) * 10;
result = 10 * 4;
result++; // Same result = result + 1
result--; // Same result = result - 1
result += 5; // Same result = result +5
result -= 5; // Same result = result -5
result *= 5; // Same result = result *5
result /= 5; // Same result = result /5
console.log(result);

